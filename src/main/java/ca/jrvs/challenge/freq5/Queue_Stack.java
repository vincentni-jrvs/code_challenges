public class Queue_Stack {
    //Difference between Stack and Queue data structure
    //A queue is a linear data structure in which elements can be inserted only from one side of the list,
    // and the elements can be deleted only from the other side called the front. This is a FIFO structure.
    //Insertion and deletion in queues takes place from the opposite ends of the list.

    //Stacks in the other hand are based on the LIFO principle.
    // The element inserted at the last, is the first element to come out of the list.
    //Insertion and deletion in stacks takes place only from one end of the list called the top.
}
